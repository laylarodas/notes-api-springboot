package dev.layla.notesapi.user.dto;

import java.time.LocalDateTime;

public class UserResponse {

    private Long id;
    private String name;
    private String email;
    private LocalDateTime createdAt;
    private int notesCount;

    public UserResponse() {}

    public UserResponse(Long id, String name, String email, LocalDateTime createdAt, int notesCount) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.createdAt = createdAt;
        this.notesCount = notesCount;
    }

    public Long getId() { return id; }
    public void setId(Long id) { this.id = id; }

    public String getName() { return name; }
    public void setName(String name) { this.name = name; }

    public String getEmail() { return email; }
    public void setEmail(String email) { this.email = email; }

    public LocalDateTime getCreatedAt() { return createdAt; }
    public void setCreatedAt(LocalDateTime createdAt) { this.createdAt = createdAt; }

    public int getNotesCount() { return notesCount; }
    public void setNotesCount(int notesCount) { this.notesCount = notesCount; }
}

